#include "inputdata.h"
/**************************************************************************************************/
InputData::InputData() = default;
/**************************************************************************************************/
InputData::InputData(QVector<QVector<int> > &g, QVector<int> &prev, int& s, int& f, int& n) : g(g), prev(prev), s(s), f(f), n(n) {}
/**************************************************************************************************/
QVector<QVector<int> >& InputData::getG()
{
  return this->g;
}
/**************************************************************************************************/
void InputData::setG(QVector<QVector<int> > &g)
{
  this->g = g;
}
/**************************************************************************************************/
QVector<int>& InputData::getPrev()
{
  return this->prev;
}
/**************************************************************************************************/
void InputData::setStart(const int s)
{
  this->s = s;
}
/**************************************************************************************************/
void InputData::setFinish(const int f)
{
  this->f = f;
}
/**************************************************************************************************/
void InputData::setN(const int n)
{
  this->n = n;
}
/**************************************************************************************************/
int& InputData::getN()
{
  return this->n;
}
/**************************************************************************************************/
int& InputData::getStart()
{
  return this->s;
}
/**************************************************************************************************/
int& InputData::getFinish()
{
  return this->f;
}
