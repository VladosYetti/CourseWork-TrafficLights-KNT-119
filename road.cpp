#include "road.h"
/**************************************************************************************************/
Road::Road(QObject *parent) : Root(parent){}
/**************************************************************************************************/
TrafficLights *Road::getLeft()
{
  return this->left;
}
/**************************************************************************************************/
TrafficLights *Road::getRight()
{
  return this->right;
}
/**************************************************************************************************/
void Road::setLeft(TrafficLights *left)
{
  this->left  =  left ;
}
/**************************************************************************************************/
void Road::setRight(TrafficLights *right)
{
  this->right = right;
}
/**************************************************************************************************/
QLineF Road::getLine()
{
  QLineF line;
  line.setP1(this->left->scenePos());
  line.setP2(this->right->scenePos());
  return line;
}
